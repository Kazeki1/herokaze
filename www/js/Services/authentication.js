(function () {
  angular.module('app')
    .service('authenticationService', function ($sessionStorage, $localStorage, $firebaseObject, $firebaseAuth, $q, $state) {

      this.login = login;
      this.logout = logout;
      this.createUserFromEmail = createUserFromEmail;
      this.initialCheck = initialCheck;
      var user;

      /**
       * @loghen41 createUserFromEmail() creates a user in firebase based on the email and password given to it
       * @loghen41 it assumes you have already validated the password
       * @loghen41 it returns a promise that contines the user object generated by firebase, or a creation error from firebase
       * @param email
       * @param password
         * @returns {Promise}
         */
      function createUserFromEmail(email, password) {
        var auth = $firebaseAuth();
        var promise = $q.defer();

        auth.$createUserWithEmailAndPassword(email, password)
          .then(function (firebaseUser) {
            var ref = firebase.database().ref("users");
            var profileRef = ref.child(firebaseUser.uid);
            var theUser = $firebaseObject(profileRef);
            var userStorage = window.localStorage.getItem('firebase:authUser:AIzaSyDtNbp-weG3kHrkLuhl6f9Ymy5JMQ0F8W8:[DEFAULT]');
            user = JSON.parse(userStorage);

            theUser.$loaded().then(
              profileRef.set({
                displayName: firebaseUser.displayName,
                email: firebaseUser.email,
                photoURL: firebaseUser.photoURL
              })
            );
            promise.resolve(user);
          })
          .catch(function (creationError) {
            promise.reject(creationError)
          });

        return promise.promise;

      }

      /**
       * @loghen41 initialCheck() checks for the user object, and either returns it, sets is and returns it, or routes you to the login page to make the user login
       * @returns {*}
         */
      function initialCheck() {
        if (user) {
          return user;
        }
        else if (window.localStorage.getItem('firebase:authUser:AIzaSyDtNbp-weG3kHrkLuhl6f9Ymy5JMQ0F8W8:[DEFAULT]')) {
          var userStorage = window.localStorage.getItem('firebase:authUser:AIzaSyDtNbp-weG3kHrkLuhl6f9Ymy5JMQ0F8W8:[DEFAULT]');
          user = JSON.parse(userStorage);
          return user;
        }
        else {
          $state.go("tabs.login")
        }
      }

      /**
       * @loghen41 login() allows the user to login, it can either be achieved through a provider (facebook, google, etc...)
       * @loghen41 If a provider is not given, it can be done through an email and password, provided they are already established as credentials in the system
       * @loghen41 the return object is either a user object, or a loginError with a message
       * @param provider
       * @param email
       * @param password
         * @returns {Promise}
         */
      function login(provider, email, password) {

        var auth = $firebaseAuth();

        var promise = $q.defer();

        if (email && password) {

          auth.$signInWithEmailAndPassword(email, password)
            .then(function (loginSuccess) {
              var userStorage = window.localStorage.getItem('firebase:authUser:AIzaSyDtNbp-weG3kHrkLuhl6f9Ymy5JMQ0F8W8:[DEFAULT]');
              user = JSON.parse(userStorage);
              promise.resolve(user);
            })
            .catch(function (loginError) {
              promise.reject(loginError);
            })

        }

        else {

          auth.$signInWithPopup(provider)

            .then(function (firebaseUser) {
              var userStorage = window.localStorage.getItem('firebase:authUser:AIzaSyDtNbp-weG3kHrkLuhl6f9Ymy5JMQ0F8W8:[DEFAULT]');
              user = JSON.parse(userStorage);
              var ref = firebase.database().ref("users");
              var profileRef = ref.child(firebaseUser.user.uid);
              var theUser = $firebaseObject(profileRef);

              theUser.$loaded().then(function () {
                  if (!theUser.displayName) {
                    profileRef.set({
                      displayName: firebaseUser.user.displayName,
                      email: firebaseUser.user.email,
                      photoURL: firebaseUser.user.photoURL
                    })
                  }
                }
              );
               $state.reload('tabs');
              promise.resolve(user)
            }).catch(function (error) {
            promise.reject(error);
          });

        }
        return promise.promise;
      }

      /**
       * @loghen41 logout() basically logs the user out of firebase, and reroutes the individual to the tabs.login, and sets the user as undefined in this model
       * @returns {undefined|*}
         */
      function logout() {
        var auth = $firebaseAuth();
        auth.$signOut();
        user = undefined;
        $state.go('tabs.login');

        return user;
      }
      
    });
})();
